/// Design system spacing tokens for Tax Lien App.
/// Based on 4pt grid with 30px page gutter.
/// Source: designsystem/colors_and_type.css
abstract final class AppSpacing {
  /// 4px - Tight gaps
  static const double xxs = 4;

  /// 8px - Small gaps
  static const double xs = 8;

  /// 12px - Component padding
  static const double sm = 12;

  /// 16px - Standard gap
  static const double md = 16;

  /// 20px - Section spacing
  static const double lg = 20;

  /// 24px - Large gaps
  static const double xl = 24;

  /// 32px - Major sections
  static const double xxl = 32;

  /// 30px - Screen edge padding (wider than typical iOS 16/20)
  static const double pageGutter = 30;

  /// 14px - Card inner padding/gap
  static const double rowGutter = 14;
}
